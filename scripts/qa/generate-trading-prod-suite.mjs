#!/usr/bin/env node
/**
 * Generate trading-prod.full.json, approval templates, and CASE_INDEX.md from catalog.
 */

import { spawnSync } from "node:child_process";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
    assertNoVariantKeysInNl,
    catalogEntryNlMatchesComposePreview,
} from "../../tests/agent-harness/lib/canonicalCaseAlignment.mjs";
import {
    getCatalogEntries,
    getCanonicalCoverageMatrix,
    TEARDOWN_DOC,
} from "../../tests/agent-harness/suites/trading-prod/trading-prod-catalog.mjs";
import {
    catalogEntryToCase,
    pickAlternatingVenueCase,
    buildCancelOrderCompose,
    EXCHANGE,
    PRODUCT_ID,
} from "../../tests/agent-harness/lib/tradingFixtures.mjs";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../..");
const OUT_DIR = path.join(ROOT, "tests/agent-harness/suites/trading-prod");
const SUITE_PATH = path.join(OUT_DIR, "trading-prod.full.json");
const TEMPLATES_PATH = path.join(OUT_DIR, "approval-templates-live-6usdt.json");
const INDEX_PATH = path.join(OUT_DIR, "CASE_INDEX.md");

function buildApprovalTemplates(cases) {
    const templates = {
        description:
            "Live 6 USDT approval templates for production trading-prod harness (BTC-USDT only).",
        defaults: {
            decision: "approved",
            feedback: "Approved for live 6 USDT harness test.",
        },
        confirmationLevel1: {
            decision: "approved",
            feedback: "L1 approved.",
        },
        confirmationLevel2: {
            decision: "approved",
            feedback: "L2 final confirm approved.",
        },
        rejectionTemplate: {
            decision: "rejected",
            feedback: "Rejected by harness operator.",
        },
        cancel_all_open_btc: {
            decision: "approved",
            feedback:
                "Approve cancel all open BTC-USDT orders (dialog-format: parameters from SSE interrupt).",
        },
    };

    for (const c of cases) {
        const key = c.approvalTemplateKey;
        if (!key || templates[key] || key === "cancel_all_open_btc") {
            continue;
        }
        if (c.approvalFormat === "dialog") {
            templates[key] = {
                decision: "approved",
                feedback: `Approve ${key} (dialog-format: parameters from SSE interrupt).`,
            };
            continue;
        }
        const params = c.compose?.params;
        if (!params) {
            continue;
        }
        templates[key] = {
            decision: "approved",
            feedback: `Approve ${key}`,
            parameters: {
                ...params,
                userId: "<filled-by-agent>",
            },
        };
    }

    return templates;
}

function expandCatalogToCases() {
    const entries = getCatalogEntries();
    const cases = [];
    for (let i = 0; i < entries.length; i++) {
        const entry = entries[i];
        const base = catalogEntryToCase(entry);
        const expanded = pickAlternatingVenueCase(base, i);
        const { title, section, mirror, ...harnessCase } = expanded;
        if (title) {
            harnessCase._title = title;
        }
        if (section) {
            harnessCase._section = section;
        }
        cases.push(harnessCase);
    }
    return cases;
}

function buildCoverageSection() {
    const matrix = getCanonicalCoverageMatrix();
    const lines = [
        "## Canonical intent coverage",
        "",
        "| Action | Dimension | Case ID |",
        "|--------|-----------|---------|",
    ];
    for (const row of matrix) {
        lines.push(`| ${row.action} | ${row.dimension} | ${row.caseId} |`);
    }
    lines.push("");
    return lines.join("\n");
}

function buildCaseIndex(cases) {
    const lines = [
        "# Trading prod case index",
        "",
        "Generated by `pnpm test:trading-prod:generate`. Edit [`trading-prod-catalog.mjs`](trading-prod-catalog.mjs).",
        "",
        "## Teardown (post-suite, not listed as cases)",
        "",
        ...TEARDOWN_DOC.steps.map((s) => `- ${s}`),
        "",
        buildCoverageSection(),
        "## Cases",
        "",
        "| ID | Title | Section | Room | Venue in prompt | Tags |",
        "|----|-------|---------|------|-----------------|------|",
    ];

    for (const c of cases) {
        const venue = (c.tags || []).includes("implicit_venue")
            ? "implicit"
            : "explicit";
        const title = c._title || c.id;
        const section = c._section || "-";
        const tags = (c.tags || []).slice(0, 5).join(", ");
        lines.push(
            `| ${c.id} | ${title} | ${section} | ${c.roomGroup} | ${venue} | ${tags} |`,
        );
    }
    lines.push("");
    return lines.join("\n");
}

function stripInternalFields(cases) {
    return cases.map((c) => {
        const { _title, _section, ...rest } = c;
        return rest;
    });
}

function validateCatalogBeforeGenerate() {
    const entries = getCatalogEntries();
    const mismatches = entries.filter((e) => !catalogEntryNlMatchesComposePreview(e));
    if (mismatches.length > 0) {
        throw new Error(
            `catalog NL/previewText mismatch: ${mismatches.map((e) => e.id).join(", ")}`,
        );
    }
    for (const entry of entries) {
        const text = entry.nl?.text ?? entry.compose?.previewText ?? "";
        if (!assertNoVariantKeysInNl(text)) {
            throw new Error(`catalog NL contains variant key: ${entry.id}`);
        }
    }
}

function runCanonicalAlignmentTests() {
    const result = spawnSync(
        "pnpm",
        [
            "exec",
            "vitest",
            "run",
            "packages/plugin-cex/__tests__/tradingProdHarnessAlignment.test.ts",
        ],
        { cwd: ROOT, stdio: "inherit" },
    );
    if (result.status !== 0) {
        throw new Error("canonical intent alignment tests failed");
    }
}

async function main() {
    await fs.mkdir(OUT_DIR, { recursive: true });

    validateCatalogBeforeGenerate();
    runCanonicalAlignmentTests();

    const cases = expandCatalogToCases();
    const templates = buildApprovalTemplates(cases);

    const suite = {
        name: "trading-prod-full",
        description:
            "Live Binance BTC-USDT production matrix (~6 USDT). Three rooms + alternating explicit/implicit venue prompts + cancel. WARNING: live writes spend real funds. Teardown runs after suite.",
        version: "2.0",
        defaults: {
            agentName: "CryptoTrader",
            roomStrategy: "perRoomGroup",
            runTeardown: true,
            hooks: ["cexAutoApprove"],
            approvalTemplates:
                "tests/agent-harness/suites/trading-prod/approval-templates-live-6usdt.json",
        },
        teardown: TEARDOWN_DOC,
        cases: stripInternalFields(cases),
    };

    await fs.writeFile(SUITE_PATH, JSON.stringify(suite, null, 2), "utf8");
    await fs.writeFile(TEMPLATES_PATH, JSON.stringify(templates, null, 2), "utf8");
    await fs.writeFile(INDEX_PATH, buildCaseIndex(cases), "utf8");

    console.log(`Wrote ${cases.length} cases -> ${SUITE_PATH}`);
    console.log(`Wrote CASE_INDEX.md (${cases.length} rows)`);
    console.log(
        `Wrote ${Object.keys(templates).length} template keys -> ${TEMPLATES_PATH}`,
    );
}

main().catch((err) => {
    console.error(err);
    process.exit(1);
});
